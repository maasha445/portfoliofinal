const errorMessage = document.getElementById("errorMessage");

function scrollRight() {
    const dataLength = HistoricChart.data.labels.length;
    if(HistoricChart.config.options.scales.x.max >= dataLength - 1) {
        HistoricChart.config.options.scales.x.min = dataLength - 12;
        HistoricChart.config.options.scales.x.max = dataLength - 1;
 
        errorMessage.innerText = "You've exceeded the limit";
    }
    else {
        errorMessage.innerText = " ";
        HistoricChart.config.options.scales.x.min += 12;
        HistoricChart.config.options.scales.x.max += 12;
    }       
    HistoricChart.update();
}


function scrollLeft() {
    if(HistoricChart.config.options.scales.x.min <= 0) {
        HistoricChart.config.options.scales.x.min = 0;
        HistoricChart.config.options.scales.x.max = 11;
       
        errorMessage.innerText = "You've exceeded the limit";
    }
    else {
        errorMessage.innerText = " ";
        HistoricChart.config.options.scales.x.min -= 12;
        HistoricChart.config.options.scales.x.max -= 12;
      
    }       
    HistoricChart.update();
}

